#Creating shapes with python turtle program

import turtle

turtle.shape("turtle")

#Draw a square
for i in range(4):
    turtle.forward(150)
    turtle.right(90)

turtle.backward(150)

#Draw a triangle
for i in range(3):
    turtle.forward(150)
    turtle.left(120)
    
